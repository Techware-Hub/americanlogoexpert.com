import { readdir,writeFile } from "node:fs/promises";
import { join,relative,sep } from "node:path";

const root=join(process.cwd(),"public","assets","portfolio");
const output=join(process.cwd(),"data","portfolio.ts");
const services=[
  {category:"3D Logo Design",slug:"3d-logo-design",folders:["3d","3d-logo-design"]},
  {category:"Animated Logo Design",slug:"animated-logo-design",folders:["animated","animated-logo-design"]},
  {category:"Abstract Logo Design",slug:"abstract-logo-design",folders:["abstract","abstract-logo-design"]},
  {category:"Mascot Logo Design",slug:"mascot-logo-design",folders:["mascot","mascot-logo-design"]},
  {category:"Gaming Logo Design",slug:"gaming-logo-design",folders:["gaming","gaming-logo-design"]},
  {category:"Clothing Logo Design",slug:"clothing-logo-design",folders:["clothing","clothing-logo-design"]},
  {category:"Signature Logo Design",slug:"signature-logo-design",folders:["signature","signature-logo-design"]},
  {category:"Branding Design",slug:"branding-design",folders:["branding","branding-design"]},
  {category:"YouTube Channel Logo",slug:"youtube-channel-logo",folders:["youtube","youtube-channel-logo"]},
  {category:"Website Development",slug:"website-development",folders:["website-development"]},
  {category:"Mobile Development",slug:"mobile-development",folders:["mobile-development"]},
];
const serviceByFolder=new Map(services.flatMap(service=>service.folders.map(folder=>[folder,service])));
const folders=await readdir(root,{withFileTypes:true});
const items=[];
let id=0;
for(const folder of folders.filter(entry=>entry.isDirectory()).sort((a,b)=>a.name.localeCompare(b.name,undefined,{numeric:true}))){
  const service=serviceByFolder.get(folder.name.toLowerCase());
  if(!service)continue;
  const files=(await readdir(join(root,folder.name),{withFileTypes:true}))
    .filter(entry=>entry.isFile()&&/\.(jpe?g|png|gif|webp|avif)$/i.test(entry.name))
    .sort((a,b)=>a.name.localeCompare(b.name,undefined,{numeric:true}));
  files.forEach((file,index)=>{
    const path="/"+relative(join(process.cwd(),"public"),join(root,folder.name,file.name)).split(sep).join("/");
    items.push({id:++id,title:`${service.category} Project ${index+1}`,category:service.category,serviceSlug:service.slug,image:path,alt:`${service.category} portfolio project ${index+1}`,featured:index===0});
  });
}
const source=`// Generated by scripts/generate-portfolio-data.mjs. Run: npm run portfolio:generate\nexport const portfolioCategories=${JSON.stringify(["All",...services.map(service=>service.category)],null,2)} as const;\nexport type PortfolioCategory=Exclude<(typeof portfolioCategories)[number],"All">;\nexport type PortfolioItem={id:number;title:string;category:PortfolioCategory;serviceSlug:string;image:string;alt:string;featured:boolean};\nexport const portfolioItems:PortfolioItem[]=${JSON.stringify(items,null,2)};\n`;
await writeFile(output,source,"utf8");
console.log(`Generated ${items.length} portfolio records at ${output}`);
